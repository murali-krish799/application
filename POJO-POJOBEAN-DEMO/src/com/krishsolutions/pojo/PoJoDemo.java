package com.krishsolutions.pojo;

/**
 * @author pojo demonstrate here 
 * plain old java object
 * must class as public doesn't extends and implement any class and interface
 *  varible can private or public recommended
 *  
 * 
 */
public class PoJoDemo {
	
int id;
String name;
String age;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getAge() {
	return age;
}
public void setAge(String age) {
	this.age = age;
}

	
	

}
